  let currentLang = 'ca';
  function applyLang(lang) {
    currentLang = lang;
    const t = i18n[lang];
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-t]').forEach(el => {
      const key = el.dataset.t;
      if (t[key] !== undefined) el.innerHTML = t[key];
    });
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    restartTyping(lang);
  }
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  // ── TYPING EFFECT ──
  const typingEl = document.getElementById('typingWord');
  let wi = 0, ci = 0, isDeleting = false, isPausing = false;
  let typingTimer = null;
  function restartTyping(lang) {
    clearTimeout(typingTimer);
    wi = 0; ci = 0; isDeleting = false; isPausing = false;
    typingEl.textContent = '';
    typingTimer = setTimeout(() => runTyping(lang), 600);
  }
  function runTyping(lang) {
    const words = typingWords[lang];
    const w = words[wi];
    if (isPausing) {
      isPausing = false;
      typingTimer = setTimeout(() => runTyping(lang), isDeleting ? 300 : 1600);
      return;
    }
    if (!isDeleting) {
      typingEl.textContent = w.slice(0, ++ci);
      if (ci === w.length) { isDeleting = true; isPausing = true; typingTimer = setTimeout(() => runTyping(lang), 1600); return; }
    } else {
      typingEl.textContent = w.slice(0, --ci);
      if (ci === 0) { isDeleting = false; wi = (wi + 1) % words.length; isPausing = true; typingTimer = setTimeout(() => runTyping(lang), 300); return; }
    }
    typingTimer = setTimeout(() => runTyping(lang), isDeleting ? 55 : 95);
  }
  typingTimer = setTimeout(() => runTyping('ca'), 1000);

