  // Lightbox intern per a imatges
  function openLightbox(src, alt) {
    let lb = document.getElementById('modal-lb');
    if (!lb) {
      lb = document.createElement('div');
      lb.className = 'mlb'; lb.id = 'modal-lb';
      lb.innerHTML = `<button class="mlb-close" id="mlb-close">✕ TANCAR</button><img />`;
      document.body.appendChild(lb);
      document.getElementById('mlb-close').onclick = () => lb.classList.remove('open');
      lb.addEventListener('click', e => { if (e.target === lb) lb.classList.remove('open'); });
    }
    lb.querySelector('img').src = src;
    lb.querySelector('img').alt = alt || '';
    lb.classList.add('open');
  }

